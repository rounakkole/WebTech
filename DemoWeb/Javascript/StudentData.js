class StudentData{
    get id(){return this._id;}
    set id(id){
        this._id=id;
    }

    get name(){return this._name;}
    set name(name){
         this._name=name;
    }

    get math(){return this._math;}
    set math(math){
       this._math=math;
    }

    get physics(){return this._physics;}
    set physics(physics){
       this._physics=physics;
    }

    get chemistry(){return this._chemistry;}
    set chemistry(chemistry){
       this._chemistry=chemistry;
    }

    get english(){return this._english;}
    set english(english){
       this._english=english;
    }

    get hindi(){return this._hindi;}
    set hindi(hindi){
       this._hindi=hindi;
    }

    get percentage(){return this._percentage;}
    set percentage(percentage){
       this._percentage=percentage;
    }

    get timestamp(){return this._timestamp;}
    set timestamp(timestamp){
       this._timestamp=timestamp;
    }

}


